    private void viewDetailBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailBtnActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = viewSignsTable.getSelectedRow();
        
        if(selectedRow >= 0){
            VitalSigns vs = (VitalSigns)viewSignsTable.getValueAt(selectedRow, 0);
            
            tempeTextField.setText(String.valueOf(vs.getTemperature()) );
            bloPreTextField.setText(String.valueOf(vs.getBloodPresure()) );
            pluseTextField.setText(String.valueOf(vs.getPulse()) );
            dateTextField.setText(vs.getDate() );
            
            
            
            
        }else{
            JOptionPane.showMessageDialog(null, "Please select any row");
        }
        
        
        
        
        
    }//GEN-LAST:event_viewDetailBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        
                
        int selectedRow = viewSignsTable.getSelectedRow();
        
        if(selectedRow >= 0){
            VitalSigns vs = (VitalSigns)viewSignsTable.getValueAt(selectedRow, 0);
            vsh.deleteVitals(vs);
            JOptionPane.showMessageDialog(null, "Vital Sign has been deleted");
            populateTable();
            
     
        }else{
            JOptionPane.showMessageDialog(null, "Please select any row");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed


