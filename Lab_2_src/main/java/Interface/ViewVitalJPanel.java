    public void populateTable(){
        
        DefaultTableModel dtm = (DefaultTableModel)viewSignsTable.getModel();
        dtm.setRowCount(0);
        
        for(VitalSigns vs : vsh.getVitalSignHistory()){
            
            Object row[] = new Object[2];
            row[0] = vs;
            row[1] = vs.getBloodPresure();
            dtm.addRow(row);
            
        }
        
        
        
    }

