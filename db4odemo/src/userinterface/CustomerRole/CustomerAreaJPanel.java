    private void requestTestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTestJButtonActionPerformed
        
        int row = workRequestJTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Pls select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return; 
        }
        WorkRequest selectWorkRequest = (WorkRequest)workRequestJTable.getValueAt(row, 0);
        
        RequestLabTestJPanel rltjp = new RequestLabTestJPanel(userProcessContainer, selectWorkRequest);
        userProcessContainer.add("RequestLabTestJPanel", rltjp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
    }//GEN-LAST:event_requestTestJButtonActionPerformed

