    public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();

        model.setRowCount(0);
        
        
        for (WorkRequest w : userAccount.getWorkQueue().getWorkRequestList()) {
            Object row[] = new Object[4];
            row[0] = w;
            row[1] = w.getReceiver();
            row[2] = w.getStatus();
            row[3] = w.getResult();
            
            model.addRow(row);
       
        }
    }
    
