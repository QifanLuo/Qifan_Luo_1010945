    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = infoTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Pls select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        Customer customer = (Customer)infoTable.getValueAt(row, 0);
        customerDirectory.getCustomerList().remove(customer);
        ecosystem.getEmployeeDirectory().getEmployeeList().remove(customer.getUser().getEmployee());
        ecosystem.getUserAccountDirectory().getUserAccountList().remove(customer.getUser());
        JOptionPane.showMessageDialog(null, "Successfully delete customer!");
        populateTable();
        
    }//GEN-LAST:event_jButton3ActionPerformed

