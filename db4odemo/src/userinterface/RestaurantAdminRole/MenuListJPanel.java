    public void populateTable(){
        
        DefaultTableModel model = (DefaultTableModel) MenuListTable.getModel();

        model.setRowCount(0);
        
        AdminRole role = (AdminRole) userAccount.getRole();
        
        
        for (MenuItem m :role.getRestaurant().getMenulist().getMenulist() ) {
            Object row[] = new Object[3];
            row[0] = m;
            row[1] = m.getPrice();
            row[2] = m.getAvailNum();
            model.addRow(row);
        }
        
    }

