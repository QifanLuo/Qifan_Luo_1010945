    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int row = MenuListTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Pls select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        MenuItem mi = (MenuItem)MenuListTable.getValueAt(row, 0);
        AdminRole role = (AdminRole) userAccount.getRole();
        role.getRestaurant().getMenulist().getMenulist().remove(mi);
        JOptionPane.showMessageDialog(null, "MenuItem removed successfully!");
        populateTable(); 
    }//GEN-LAST:event_deleteBtnActionPerformed

